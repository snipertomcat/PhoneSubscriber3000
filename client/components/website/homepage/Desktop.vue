            });
        },
        mounted: function () {
            const dictionary = {
                es: {
                    messages: {
                        company_phone: () => !this.badPhone ? `El número de teléfono sólo debe contener 10 dígitos.` : `El número de teléfono debe contener solo dígitos`,
                    },
                    attributes: {
                        name: 'nombre',
                        email: 'correo electrónico',
                        phone: 'teléfono',
                        company: 'nombre de la empresa',
                    },
                    custom: {
                        name: {
                            required: 'El nombre es obligatorio.',
                            alpha_spaces: 'El nombre sólo puede contener letras y espacios.',
                            min: 'La longitud mínima del nombre es de 2 caracteres.',
                            max: 'La longitud máxima del nombre es de 50 caracteres.',
                        },
                        email: {
                            required: 'La dirección de correo electrónico es obligatoria.',
                            email: 'No es una dirección de correo electrónico válida.',
                            regex: 'No es una dirección de correo electrónico válida.'
                        },
                        phone: {
                            required: 'El número de teléfono es obligatorio.',
                            digits: 'El número de teléfono sólo debe contener 10 dígitos.',
                            numeric: 'El número de teléfono sólo debe contener números.',
                        },
                        company: {
                            required: 'El nombre de la empresa es obligatorio.',
                            max: 'La longitud máxima del nombre de la empresa son 25 caracteres.'
                        },
                    }
                },
                en: {
                    messages: {
                        company_phone: () => !this.badPhone ? `The phone number must only contain 10 digits.` : `The phone number must only digits.`,
                    }
                }
            };

            VeeValidate.Validator.localize(dictionary);
            VeeValidate.Validator.localize('es', es);

            var _this = this;

            $(function () {
                let _center = {
                    width: 400,
                    height: 150,
                    marginLeft: 0,
                    marginTop: 0,
                    marginRight: 0,
                    opacity: 1
                };
                let _left = {
                    width: 400,
                    height: 150,
                    marginLeft: 0,
                    marginTop: 50,
                    marginRight: -150,
                    opacity: 0.70
                };
                let _right = {
                    width: 400,
                    height: 150,
                    marginLeft: -150,
                    marginTop: 50,
                    marginRight: 0,
                    opacity: 0.70
                };
                let _outLeft = {
                    width: 200,
                    height: 150,
                    marginLeft: 150,
                    marginTop: 75,
                    marginRight: -200,
                    opacity: 0
                };
                let _outRight = {
                    width: 200,
                    height: 150,
                    marginLeft: -200,
                    marginTop: 75,
                    marginRight: 50,
                    opacity: 0
                };

                $('#carousel').carouFredSel({
                    width: 900,
                    height: 200,
                    align: false,
                    items: {
                        visible: 3,
                        width: 150
                    },
                    scroll: {
                        items: 1,
                        duration: 400,
                        pauseOnHover: true,
                        onBefore: function( data ) {
                            data.items.old.eq( 0 ).animate(_outLeft);
                            data.items.visible.eq( 0 ).animate(_left);
                            data.items.visible.eq( 1 ).animate(_center);
                            data.items.visible.eq( 2 ).animate(_right).css({ zIndex: 1 });
                            data.items.visible.eq( 2 ).next().css(_outRight).css({ zIndex: 0 });

                            setTimeout(function() {
                                data.items.old.eq( 0 ).css({ zIndex: 1 });
                                data.items.visible.eq( 0 ).css({ zIndex: 2 });
                                data.items.visible.eq( 1 ).css({ zIndex: 3 });
                                data.items.visible.eq( 2 ).css({ zIndex: 2 });
                            }, 400);
                        }
                    }
                });

                $('#carousel').children().eq( 0 ).css(_left).css({ zIndex: 2 });
                $('#carousel').children().eq( 1 ).css(_center).css({ zIndex: 3 });
                $('#carousel').children().eq( 2 ).css(_right).css({ zIndex: 2 });
                $('#carousel').children().eq( 3 ).css(_outRight).css({ zIndex: 1 });
            });

            $(document).ready(function () {
                $('#fullpage').fullpage({
                    autoScrolling: true,
                    sectionSelector: 'section'
                })
                $(window).on('orientationchange', function (event) {
                    _this.updateContactForm()
                })
                $('.btn-request').on('click', function (e) {
                    $('#fullpage').fullpage.moveTo(4)
                })
            })
        },
        methods: {
            updateContactForm: function () {
                let fields = document.querySelectorAll('section#four .contact-form .field')

                if (document.querySelector('section#four .content .we-contact-u').style.display === 'none') {
                    document.querySelector('section#four .content .contact-form').style.display = 'block'
                    _.each(fields, field => {
                        field.querySelector('input').value = null
                        field.style.display = 'flex'
                    })
                }
            },
            setActive: function(event) {
                let target = event.target.dataset.target
                let features = document.querySelectorAll('.apithy-features .feature')
                let descriptions = document.querySelectorAll('section#three .apithy-features-descriptions .description')

                _.each(descriptions, description => {
                    description.classList.remove('active')
                    if (description.classList.contains(target)) {
                        description.classList.add('active')
                    }
                })
                _.each(features, feature => {
                    feature.classList.remove('active')
                    if (feature.classList.contains(target)) {
                        feature.classList.add('active')
                    }
                })
            },
            send: function () {
                this.$validator.validateAll().then(result => {
                    if (result) {
                        axios({
                            method: 'POST',
                            url: '/contacts',
                            data: this.form
                        })
                            .then(response => {
                                document.querySelector('section#four .content .talking-us').style.display = 'none'
                                document.querySelector('section#four .content .contact-form').style.cssText = 'display:none !important'
                                document.querySelector('section#four .content .we-contact-u').style.display = 'flex'
                            })
                            .catch(error => { console.error(error) })
                    }
                });
            }
        },
        data: function () {
            return {
                form: {
                    name: null,
                    company: null,
                    email: null,
                    phone: null,
                    number_employees: null
                },
                validators: {
                    name: {
                        required: true,
                        alpha_spaces: true,
                        min: 2,
                        max: 50
                    },
                    company: {
                        required: true,
                        alpha_spaces: true,
                        min: 2,
                        max: 50
                    },
                    email: {
                        required: true,
                        email: true,
                    },
                    phone: {
                        required: false,
                        digits: 10,
                        numeric: true,
                    },
                    employees: {
                        required: true,
                    },
                }
            }
        }
    }
